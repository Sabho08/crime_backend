BNS_SECTIONS = [111, 303, 115, 126, 70, 103, 310, 320]
WEAPON_TYPES = ['Firearm', 'Sharp Object', 'Blunt Object', 'None', 'Explosive']
TIME_OF_DAY = ['Morning', 'Afternoon', 'Evening', 'Night']

def generate_crime_data(num_records=1000):
    data = []
    for i in range(num_records):
        loc = random.choice(COORDINATE_POOL)
        record = {
            'FIR_UID': f'FIR-{2024}-{1000+i}',
            'Latitude': loc[0],
            'Longitude': loc[1],
            'BNS_Section': random.choice(BNS_SECTIONS),
            'Weapon_Type': random.choice(WEAPON_TYPES),
            'Time_of_Day': random.choice(TIME_OF_DAY),
            'Dist_to_PS': round(random.uniform(0.1, 5.0), 2)  # Distance in km
        }
        data.append(record)
    
    df = pd.DataFrame(data)
    df.to_csv('crime_data.csv', index=False)
    print(f"Generated {num_records} records in crime_data.csv")

if __name__ == "__main__":
    generate_crime_data()
